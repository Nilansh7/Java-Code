// use of for loop first program, illustration of every step.

import java.util.Scanner;
public class ForLoopIllustration{
    public static void main(String args[]){
        for(int counter = 0; counter < 3; counter=counter+1){          //for(int initialise; termination; updation)
            System.out.println("Hello World");                       // initialise => where to start
        }                                                              // termination => where to stop
    }                                                                  // updation => update between the starting and ending point
}